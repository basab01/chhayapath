<ul>PSA Definitions<br>
General: Entries must originate as photographs (image-capture of objects via light sensitivity)
made by the entrant on photographic emulsion or acquired digitally. By virtue of submitting an
entry, the entrant certifies the work as his/her own and permits the sponsors to reproduce all
or part of the entered material free of charge for publication and / or display in media related to
the exhibition. This may include low resolution posting on a website. The exhibition assumes
no liability for any misuse of copyright. Images may be altered, either digitally or otherwise, by
the maker and artwork or computer graphics created by the entrant may be incorporated if
the photographic contents predominate, without violating Section definition as laid down by
PSA. Creative is defined as "altered reality".</ul><br><br>

<ul>Monochrome – (PSA)<br>
An image is considered to be Monochrome only if it gives the impression of having no colour
(i.e. contains only shades of grey which can include pure black and pure white) OR it gives
the impression of being a grey scale image that has been toned in one colour across the
entire image. (For example by sepia, red, gold, etc.)
This definition applies to all sections with the exception that toned images are NOT allowed in
Nature, Photojournalism or Photo Travel.</ul><br><br>

<ul>Photo Travel:<br>
A Photo Travel image expresses the characteristic features or culture of a land as they are
found naturally. There are no geographic limitations. Images from events or activities arranged
specifically for photography, or of subjects directed or hired for photography are not
appropriate. Close up pictures of people or objects must include features that provide
information about the environment. Techniques that add, relocate, replace or remove any
element of the original image, except by cropping, are not permitted. The only allowable
adjustments are removal of dust or digital noise, restoration of the appearance of the original
scene, and complete conversion to grey scale monochrome. Other derivations, including
infrared, are not permitted. The final image must look natural</ul><br><br>

<ul>PSA, FIAP, and RPS Nature Definition:<br>
Nature photography is restricted to the use of the photographic process to depict all branches
of natural history, except anthropology and archaeology, in such a fashion that a well-informed
person will be able to identify the subject material and to certify its honest presentation. The
story telling value of a photograph must be weighed more than the pictorial quality while
maintaining high technical quality. Human elements shall not be present, except where those
human elements are integral parts of the nature story such as nature subjects, like barn owls
or storks, adapted to an environment modified by humans, or where those human elements
are in situations depicting natural forces, like hurricanes or tidal waves. Scientific bands,
scientific tags or radio collars on wild animals are permissible. Photographs of human created
hybrid plants, cultivated plants, feral animals, domestic animals, or mounted specimens are
ineligible, as is any form of manipulation that alters the truth of the photographic statement. No
techniques that add, relocate, replace, or remove pictorial elements except by cropping are
permitted. Techniques that enhance the presentation of the photograph without changing the
nature story or the pictorial content, or without altering the content of the original scene, are
permitted including HDR, focus stacking and dodging/burning. Techniques that remove
elements added by the camera, such as dust spots, digital noise, and film scratches, are
allowed. Stitched images are not permitted. The final image must look natural. Colour images
may be converted to greyscale monochrome. Infrared images, either direct-captures or
derivations, are not allowed.<br>
Images used in Nature Photography competitions may be divided in two classes: Nature and
Wildlife.<br><br>

Definition of Wildlife Photography<br><br>
Images entered in Wildlife sections meeting the Photography Definition above can have landscapes, geologic formations, weather phenomena, and extant zoological or botanical organisms free and unrestrained in a natural or adopted habitat, landscapes (initial capital letter removed), geologic formations, photographs of zoo or game farm animals, or of any extant zoological or botanical species taken under controlled conditions are not eligible in Wildlife sections. Wildlife is not limited to mammals, birds and insects. Marine subjects and botanical subjects (including fungi and algae) taken in the wild are suitable wildlife subjects as are carcasses of extant species.
Wildlife images may be entered in Nature sections of Exhibitions.<br><br>

With the sole act of submitting his/her images or files to a salon under FIAP Patronage,
the entrant accepts without exception and with no objection that the submitted images
can be investigated by FIAP to establish if these obey to FIAP regulations and
definitions even if the entrant is not a member of FIAP; that FIAP will use any means at
its disposal for this undertaking; that any refusal to cooperate with FIAP or any refusal
to submit the original files as captured by the camera, or failure to provide sufficient
evidence, will be sanctioned by FIAP and that in case of sanctions following the noncompliance
with FIAP regulations, the name of the entrant will be released in any form
useful to inform the breaches of the rules. It is recommended to leave the EXIF data in
the submitted files intact in order to ease eventual investigations.
</ul><br><br>




